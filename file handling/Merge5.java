//program to remove duplicacy from the file
import java.io.*;
class Merge5
{
	String[] store;
	public static void main(String... args)
	{ 
		BufferedReader br=new BufferedReader("File.txt");
		String s=br.readLine();
		int i=0;
		while(s!=null)
		{
			store[i++]=s;
			s=br.readLine();
		}
		
		
	}
}